
num1 = int(input('Вводим число 1:'))
num2 = int(input('Вводим число 2:'))
             
print(num1, 'умножить на', num2, 'равно', num1 * num2)

print(num1, 'разделить на', num2, 'равно', num1 / num2)

print(num1, 'нацело разделить на', num2, 'равно', num1 // num2)

print('Остаток от деления', num1, 'на', num2, 'равен', num1 % num2)

print(num1, 'в степени', num2, 'равно', num1 ** num2)
